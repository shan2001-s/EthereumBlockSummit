function createSpeakerDiv(speaker) {
    var speakerDiv = document.createElement("div");
    speakerDiv.classList.add("col-lg-3", "col-md-6");
  
    var innerDiv = document.createElement("div");
    innerDiv.classList.add("speaker");
    innerDiv.setAttribute("data-aos", "fade-up");
    innerDiv.setAttribute("data-aos-delay", "100");
  
    var imageDiv = document.createElement("div");
    imageDiv.classList.add("image-container");
  
    var twitterLogoDiv = document.createElement("div");
    twitterLogoDiv.classList.add("twitter-logo-container");
    twitterLogoDiv.style.position = "absolute";
    twitterLogoDiv.style.top = "0";
    twitterLogoDiv.style.right = "0";
    twitterLogoDiv.style.margin = "10px";
  
    var twitterLogo = document.createElement("img");
    twitterLogo.setAttribute("src", "assets/img/twitter-logo.png");
    twitterLogo.setAttribute("alt", "Twitter Logo");
    twitterLogo.style.height = "30px";
  
    twitterLogoDiv.appendChild(twitterLogo);
    imageDiv.appendChild(twitterLogoDiv);
  
    var image = document.createElement("img");
    image.setAttribute("src", speaker.image);
    image.setAttribute("alt", speaker.name);
    image.classList.add("img-fluid");
    image.style.cursor = "pointer";
    image.style.zIndex = -1;
    image.style.height = '300px';
    image.style.objectFit = 'cover';
    image.setAttribute("data-bs-toggle", "modal");
    image.setAttribute("data-bs-target", "#speakerModal");
  
    image.addEventListener("click", function () {
      showSpeakerDetails(speaker);
    });
  
    var logoDiv = document.createElement("div");
    logoDiv.classList.add("logo-container");
  
    var speakerLogo = document.createElement("img");
    speakerLogo.setAttribute("src", speaker.logo);
    speakerLogo.setAttribute("alt", "Speaker Logo");
    speakerLogo.classList.add("speaker-logo");
    speakerLogo.style.height = "50px";
    speakerLogo.style.position = 'absolute';
    speakerLogo.style.zIndex = 1;
    speakerLogo.style.borderRadius = "10%";
    speakerLogo.style.marginBottom = '-20px';
  
    var detailsDiv = document.createElement("div");
    detailsDiv.classList.add("details");
    detailsDiv.addEventListener("click", function () {
      showSpeakerDetails(speaker);
    });
  
    var speakerName = document.createElement("h3");
    speakerName.classList.add("speaker-name");
    speakerName.textContent = speaker.name;
    speakerName.style.cursor = "pointer";
    speakerName.setAttribute("data-bs-toggle", "modal");
    speakerName.setAttribute("data-bs-target", "#speakerModal");
  
    speakerDiv.appendChild(innerDiv);
    innerDiv.appendChild(imageDiv);
    innerDiv.appendChild(logoDiv);
    innerDiv.appendChild(detailsDiv);
    imageDiv.appendChild(image);
    logoDiv.appendChild(speakerLogo);
    detailsDiv.appendChild(speakerName);
  
    return speakerDiv;
  }
  
  function showSpeakerDetails(speaker) {
    var speakerModalLabel = document.getElementById("speakerModalLabel");
    var speakerImage = document.getElementById("speakerImage");
    var speakerName = document.getElementById("speakerName");
    var speakerDescription = document.getElementById("speakerDescription");
    var seeMoreBtn = document.getElementById("seeMoreBtn");
  
    speakerModalLabel.textContent = speaker.name;
    speakerImage.setAttribute("src", speaker.image);
    speakerImage.setAttribute("alt", speaker.name);
    speakerName.textContent = speaker.name;
    speakerDescription.textContent = speaker.description;
    seeMoreBtn.textContent = speaker.morebtn;
    seeMoreBtn.setAttribute("data-bs-dismiss", "modal");
  }
  
  function loadSpeakers() {
    var speakersContainer = document.getElementById("speakersContainer");
    var speakersRow = document.getElementById("speakersRow");
  
    speakers.forEach(function (speaker) {
      var speakerDiv = createSpeakerDiv(speaker);
      speakersRow.appendChild(speakerDiv);
    });
  }
  
  document.addEventListener("DOMContentLoaded", function () {
    loadSpeakers();
  });
  
  




